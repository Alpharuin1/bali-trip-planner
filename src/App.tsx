interface PlanViewProps {
  plan: Plan;
  color: string;
  tile: TileStyle;
  stops: Stop[];
  legs: ReturnType<typeof useRouteLegs>;
  themeMode: ThemeMode;
  hoveredDayIndex: number | null;
  leftFraction: number;
  onHoverDayIndex: (i: number | null) => void;
  onMarkerClick: (dayIndex: number) => void;
  onTileChange: (s: TileStyle) => void;
  onSplitterDrag: (dx: number) => void;
  onResetSplit: () => void;
  onUpdateDay: (id: string, day: Day) => void;
  onReorderDays: (fromId: string, toId: string) => void;
  registerDayRef: (id: string, el: HTMLDivElement | null) => void;
  startDate: string;
  endDate: string;
  onStartDateChange: (v: string) => void;
  onEndDateChange: (v: string) => void;
}

const PlanView = ({
  ref,
  plan,
  color,
  tile,
  stops,
  legs,
  themeMode,
  hoveredDayIndex,
  leftFraction,
  onHoverDayIndex,
  onMarkerClick,
  onTileChange,
  onSplitterDrag,
  onResetSplit,
  onUpdateDay,
  onReorderDays,
  registerDayRef,
  startDate,
  endDate,
  onStartDateChange,
  onEndDateChange,
}: PlanViewProps & { ref?: React.Ref<HTMLDivElement> }) => {
  const t = tokens(themeMode);
  const dateBase = parseISO(plan.startDate);

  const sensors = useSensors(
    useSensor(PointerSensor, { activationConstraint: { distance: 5 } })
  );
  const onDragEnd = (e: DragEndEvent) => {
    const { active, over } = e;
    if (!over || active.id === over.id) return;
    onReorderDays(active.id as string, over.id as string);
  };

  return (
    <Box
      ref={ref}
      sx={{
        flex: 1,
        minHeight: 0,
        display: "flex",
      }}
    >
      <Box
        sx={{
          flex: leftFraction,
          minWidth: MIN_ITINERARY_WIDTH,
          minHeight: 0,
          display: "flex",
          flexDirection: "column",
        }}
      >
        <Paper
          elevation={0}
          sx={{
            flex: 1,
            p: 2,
            borderRadius: RADIUS_SECTION,
            boxShadow: t.cardShadow,
            display: "flex",
            flexDirection: "column",
            minHeight: 0,
            minWidth: 0,
            overflow: "hidden",
          }}
        >
          <Stack
            direction="row"
            sx={{
              flexShrink: 0,
              pb: 1.25,
              mb: 1,
              alignItems: "center",
              justifyContent: "space-between",
              flexWrap: "wrap",
              gap: 1,
            }}
          >
            <Stack direction="row" spacing={1} sx={{ alignItems: "center" }}>
              <Box
                sx={{
                  width: 12,
                  height: 12,
                  borderRadius: "50%",
                  bgcolor: color,
                  border: "2px solid #fff",
                  boxShadow: `0 2px 6px ${color}66`,
                }}
              />
              <Typography variant="h6">Bali itinerary</Typography>
            </Stack>
            <Stack direction="row" spacing={1} sx={{ alignItems: "center", flexWrap: "wrap" }}>
              <TextField
                size="small"
                type="date"
                value={startDate}
                onChange={(e) => {
                  const v = e.target.value;
                  onStartDateChange(v);
                  if (parseISO(v) > parseISO(endDate)) onEndDateChange(v);
                }}
                {...typingFieldKeyDownProps}
                sx={{ width: 142 }}
              />
              <Typography sx={{ color: "text.secondary", fontSize: 13 }}>→</Typography>
              <TextField
                size="small"
                type="date"
                value={endDate}
                onChange={(e) => {
                  const v = e.target.value;
                  if (parseISO(v) < parseISO(startDate)) return;
                  onEndDateChange(v);
                }}
                {...typingFieldKeyDownProps}
                sx={{ width: 142 }}
              />
              <Typography variant="body2" sx={{ color: "text.secondary" }}>
                {plan.days.length} day{plan.days.length > 1 ? "s" : ""}
              </Typography>
            </Stack>
          </Stack>

          <Box
            sx={{
              flex: 1,
              minHeight: 0,
              display: "flex",
              flexDirection: "column",
            }}
          >
            <DndContext
              sensors={sensors}
              collisionDetection={closestCenter}
              onDragEnd={onDragEnd}
            >
              <SortableContext
                items={plan.days.map((d) => d.id)}
                strategy={horizontalListSortingStrategy}
              >
                <Box
                  sx={{
                    display: "flex",
                    gap: 1.5,
                    overflowX: "auto",
                    overflowY: "visible",
                    pb: 1,
                    flex: 1,
                    alignItems: "stretch",
                    minWidth: 0,
                    minHeight: 0,
                    scrollbarGutter: "stable",
                  }}
                >
                  {plan.days.map((day, i) => (
                    <SortableItem key={day.id} id={day.id} fill>
                      {(handle) => (
                        <Box
                          ref={(el: HTMLDivElement | null) =>
                            registerDayRef(day.id, el)
                          }
                          sx={{ height: "100%" }}
                        >
                          <DayBlock
                            index={i}
                            date={addDays(dateBase, i)}
                            day={day}
                            mode={themeMode}
                            cardRadius={RADIUS_DAY_CARD}
                            dragHandle={handle}
                            onHover={(h) => onHoverDayIndex(h ? i : null)}
                            onChange={(d) => onUpdateDay(day.id, d)}
                          />
                        </Box>
                      )}
                    </SortableItem>
                  ))}
                </Box>
              </SortableContext>
            </DndContext>
          </Box>
        </Paper>
      </Box>

      <Splitter onDrag={onSplitterDrag} onDoubleClick={onResetSplit} />

      <Box
        sx={{
          flex: 1 - leftFraction,
          minWidth: MIN_MAP_WIDTH,
          minHeight: 0,
          display: "flex",
          flexDirection: "column",
        }}
      >
        <Paper
          elevation={0}
          sx={{
            flex: 1,
            p: 0,
            borderRadius: RADIUS_SECTION,
            boxShadow: t.cardShadow,
            overflow: "hidden",
            minHeight: 0,
            minWidth: 0,
            position: "relative",
          }}
        >
          <TripMap
            stops={stops}
            color={color}
            tile={tile}
            legs={legs}
            hoveredDayIndex={hoveredDayIndex}
            onMarkerClick={onMarkerClick}
          />
          <MapLegend color={color} />
          <TileSwitcher value={tile} onChange={onTileChange} />
        </Paper>
      </Box>
    </Box>
  );
};

